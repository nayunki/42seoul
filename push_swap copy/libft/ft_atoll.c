#include "libft.h"

int	ft_atoll(const char *str)
{
	int					n;
	unsigned long long	res;

	n = 1;
	res = 0;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			n *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = (*str - '0') + (res * 10);
		str++;
	}
	return (n * res);
}
